"""
En python existen los siguientes tipos de datos:
1. Enteros y largos: int OR long
2. Flotantes o reales: float
3. Números complejos: Parte real e imaginaria complex
4. Cadenas: Texto encerrado entre comillas simples o dobles
5. Booleanos: Puede tener dos valores, True OR False, se expresan mediante bool
"""

#Tipo de dato entero o largo

numero = 15
print(numero, type(numero))

#Tipo de dato flotante o real

numero_flotante = 15.5

print(numero_flotante, type(numero_flotante))

#Tipo de dato número complejo

numero_complejo = 5 + 6j

print(numero_complejo, type(numero_complejo))

#Tipo de dato String

nombre = "Damian"

print(nombre, type(nombre))

#Tipo de dato Booleano

verdadero_falso = 3 == 3

print(verdadero_falso, type(verdadero_falso))