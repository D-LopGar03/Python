# El método capitalize() en Python es una función de cadena que se utiliza para capitalizar la primera letra de una cadena. 
# Devuelve una nueva cadena en la que la primera letra está en mayúscula y el resto de la cadena permanece en minúsculas.

cadena1 = "hOLA MUNdo"
print("Sin método capitalize:", cadena1)
print("Con método capitalize:",cadena1.capitalize(),"\n")

cadena2 = "El VIAje Es lA RecoMPENsa"
print("Sin método capitalize:", cadena2)
print("Con método capitalize:",cadena2.capitalize(),"\n")

cadena3 = "eS MEDio DÍa y HacE MuchO CaLOR"
print("Sin método capitalize:", cadena3)
print("Con método capitalize:",cadena3.capitalize(),"\n")